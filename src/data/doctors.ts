// Group doctors by department
export const getDoctorsByDepartment = (): Record<string, Doctor[]> => {
  const grouped: Record<string, Doctor[]> = {};
  doctors.forEach((doc) => {
    if (!grouped[doc.department]) {
      grouped[doc.department] = [];
    }
    grouped[doc.department].push(doc);
  });
  return grouped;
};

// Search doctors by symptom keywords
export const searchDoctorsBySymptom = (query: string): Doctor[] => {
  if (!query.trim()) return [];
  const q = query.toLowerCase().trim();
  const terms = q.split(/\s+/);
  return doctors.filter((doc) =>
    terms.some(
      (term) =>
        doc.symptoms.some((s) => s.includes(term)) ||
        doc.specialty.toLowerCase().includes(term) ||
        doc.department.toLowerCase().includes(term) ||
        doc.name.toLowerCase().includes(term)
    )
  );
};
